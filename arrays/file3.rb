## This file contains example of Array slicing in Ruby.

## Let's consider an array with element "Bus", "Car", "Bike", "Bicycle", "Taxi", "Truck"
vehicles = ["Bus", "Car", "Bike", "Bicycle", "Taxi", "Truck"]

## To access 1st 3 element of array, i.e. "Bus", "Car", "Bike"
puts vehicles[0..2]  		
# => 
# "Bus"  
# "Car"
# "Bike"

print vehicles[0..2] 
# => ["Bus", "Car", "Bike"]
